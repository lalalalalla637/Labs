package org.example.task0;

public class Coordinate {

    private double L;
    private double W;

    public Coordinate(double l, double w) {
        L = l;
        W = w;
    }

    public double getL() {
        return L;
    }

    public void setL(double l) {
        L = l;
    }

    public double getW() {
        return W;
    }

    public void setW(double w) {
        W = w;
    }

}
